  <pre style="font-family: 'Courier New', Courier, monospace; color: #009CC5; font-size: 16px; line-height: 1.6; background-color: #000; padding: 20px;">
<>    Qu'est-ce que le RGPD ?

Le Règlement Général sur la Protection des Données (RGPD) est une réglementation 
européenne entrée en vigueur le 25 mai 2018, visant à renforcer la protection 
des données personnelles des citoyens de l’Union européenne. Ce règlement impose
des obligations aux entreprises, administrations et organisations qui collectent, 
traitent ou stockent des données personnelles. En France, la mise en œuvre du RGPD 
est encadrée par la Commission Nationale de l'Informatique et des Libertés (CNIL), 
qui veille à la conformité des traitements de données avec la loi.

<>    Vos droits en tant qu'utilisateur

En vertu du RGPD, vous disposez de plusieurs droits concernant vos données 
personnelles. Voici vos principaux droits :

>Droit d'accès : Vous avez le droit de savoir quelles données nous collectons, 
pourquoi et comment elles sont utilisées.
>Droit de rectification : Vous pouvez demander la correction de vos données 
personnelles si elles sont inexactes ou incomplètes.
>Droit à l’effacement : Vous pouvez demander la suppression de vos données 
dans certains cas (par exemple, si elles ne sont plus nécessaires).
>Droit à la portabilité : Vous pouvez demander que vos données vous soient 
transmises ou transférées à une autre organisation.
>Droit d’opposition : Vous avez le droit de vous opposer au traitement de 
vos données dans certaines situations, notamment à des fins de prospection 
commerciale. 
>Droit à la limitation du traitement : Vous pouvez demander 
que vos données soient temporairement "gelées" (non utilisées mais conservées).
>Droit de retrait du consentement : Si vous avez donné votre consentement pour 
un traitement spécifique, vous pouvez le retirer à tout moment.

<>    Comment nous collectons et utilisons vos données

Lorsque vous naviguez sur notre site, nous pouvons collecter certaines données 
personnelles, telles que :

>Données collectées via des formulaires : nom, prénom, adresse email, 
numéro de téléphone, etc.
>Données techniques : adresse IP, type de navigateur, pages visitées, etc.

Toutes les données collectées sont traitées de manière transparente et sécurisée, 
uniquement à des fins spécifiques (gestion des demandes, amélioration de nos 
services, personnalisation de votre expérience).

<>    Vos recours

Si vous estimez que vos droits ne sont pas respectés ou que vos données 
personnelles sont utilisées de manière abusive, vous pouvez soit :

- Nous contacter directement à l’adresse mail suivante :
contact@cnil.fr.

- Déposer une plainte auprès de la CNIL:
via leur site internet (www.cnil.fr) ou par courrier postal

<>    Engagements du MA+ corporate

Nous nous engageons à :

>Collecter uniquement les données strictement nécessaires à nos activités.
>Obtenir votre consentement clair et explicite lorsque nécessaire.
>Mettre en œuvre des mesures de sécurité pour protéger vos données 
contre tout accès non autorisé ou fuite.
>Ne jamais vendre vos données à des tiers.

>Pour toute question relative à vos données personnelles, veuillez nous 
contacter via notre formulaire de contact ou à l’adresse suivante : 
    infos@mapluscorporate.com.
